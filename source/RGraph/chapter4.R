# 개선된 mypower함수     
mypower=function(x){         # 새로운 함수 정의
    if(is.numeric(x)) {      # 숫자인지 판단
       result=c(x^3)     # 숫자인 경우 세제곱 값 구함
       
    }
    else{                    
       result=NA             # 숫자가 아닌 경우 실행
    }
    result                   # 결과 반환 
}
mypower(3)
mypower("moon")

## ----eval=FALSE----------------------------------------------------------
## result=ifelse(조건문,참일 경우 값,거짓일 경우 값)

## ------------------------------------------------------------------------
# ifelse()함수로 개선된 mypower함수     
mypower2=function(x){         
    ifelse(is.numeric(x),c(x^3),NA)
}
mypower2(3)
mypower2("moon")

## ------------------------------------------------------------------------

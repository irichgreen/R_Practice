## ------------------------------------------------------------------------
oddSum=0                # 홀수의 합을 저장할 변수, 0으로 초기화
for(i in 1:100) {       # 1부터 100까지 반복 
    if(i%%2==0) next    # 짝수(2로 나눈 나머지가 0)이면 다음 반복으로 넘어간다.
    oddSum=oddSum+i
}
oddSum

